"""
Модуль Pydantic схемы для JWT токенов.

Этот модуль определяет структуру ответа для эндпоинтов аутентификации,
возвращающих JWT токен. Схема используется для стандартизации формата
ответов при успешной аутентификации пользователя.

Основная схема:
    Token: Схема ответа с JWT токеном доступа

Зависимости:
    pydantic: BaseModel для определения схемы
"""

from pydantic import BaseModel

class Token(BaseModel):
    """
    Схема ответа с JWT токеном доступа.

    Используется в эндпоинтах аутентификации (например, /auth/login)
    для возврата сгенерированного JWT токена клиенту.

    Поля:
        access_token (str): JWT токен для доступа к защищенным эндпоинтам API.
            Содержит закодированные данные пользователя (обычно user_id и email)
            и имеет ограниченный срок действия (по умолчанию 30 дней).
            Клиент должен передавать этот токен в заголовке Authorization
            при запросах к защищенным эндпоинтам.
            Пример: "eyJhbGciOiJIUzI1NiIsInR5cCI6IkpXVCJ9..."

        token_type (str): Тип токена, всегда "bearer".
            Значение по умолчанию: "bearer"
            Соответствует стандарту OAuth 2.0 Bearer Token.
            Указывает клиенту, как использовать токен для аутентификации.

    Использование:
        - Ответ эндпоинта POST /auth/login при успешной аутентификации
        - Может использоваться в других эндпоинтах, возвращающих токены
          (например, обновление токена)

    Примечание:
        Токен должен передаваться в заголовке HTTP запроса:
        Authorization: Bearer <access_token>
    """

    access_token: str
    token_type: str = "bearer"
